public class Room implements Serializable{
    private int id, code;
    private String name;
    
    public Room(){
        
    }
    public Room(int id, int code, String name) {
        this.id = id;
        this.code = code;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    @Override
    public String toString(){
        return "Room {id="+id+"  name="+name+"  code="+code+ "}";
    }
}
