    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        int code = Integer.parseInt(txtCode.getText());
        Employee e = ctl.getEmp(code);
        if (e == null) {
            JOptionPane.showMessageDialog(this, "Can not found the employee has this code!");
        } else {
            txtCode.setText(String.valueOf(e.getCode()));
            txtName.setText(e.getName());
            txtAge.setText(String.valueOf(e.getAge()));
            txtSalary.setText(String.valueOf(e.getSalary()));
        }
    }//GEN-LAST:event_btnSearchActionPerformed

