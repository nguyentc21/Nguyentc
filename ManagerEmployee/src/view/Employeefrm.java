    void loadTable(List<Employee> list) {
        Vector column = new Vector();
        column.add("Code");
        column.add("Name");
        column.add("Age");
        column.add("Salary");
        Vector rows = new Vector();
        for (Employee e : list) {
            Vector row = new Vector();
            row.add(e.getCode());
            row.add(e.getName());
            row.add(e.getAge());
            row.add(e.getSalary());
            rows.add(row);
        }
        tblEmployee.setModel(new DefaultTableModel(rows, column));
        tblEmployee.updateUI();
        spnEmployee.setViewportView(this.tblEmployee);
    }

